public class Forloopchecking {

	public static void main(String[] args) {



for(int i=1;i<5;i++){
	//System.out.print("*");
	for(int j=1;j<i;j++){
		System.out.print("*");
	}
	System.out.println("*");
	
}

for(int i=5;i>1;i--){
	//System.out.print("*");
	for(int j=1;j<i;j++){
		System.out.print("*");
	}
	System.out.println("*");
	
}


		
		

		
	}

		
	

}
