package com.tricky.www;

public class Pattern_using_while {
	public static void main(String[] args) {
        int i=4,j,l;
        while(0<i){
            j=0;
            while(j<i){
                l=4-j;
                System.out.print(l);
            j++;
                
        }
        System.out.println();
        i--;
    }
    }
}
